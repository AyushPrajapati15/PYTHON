# Arithmetic operators
x = 10
y = 3
print("x + y = ", x + y)
print("x - y = ", x - y)
print("x * y = ", x * y)
print("x / y = ", x / y)
print("x % y = ", x % y)
print("x // y = ", x // y)
print("x ** y = ", x ** y)

# Assignment operators
a = 5
a += 2
print("a = ", a)
a -= 1
print("a = ", a)
a *= 3
print("a = ", a)

# Relational operators
b = 7
c = 12
print("b == c is ", b == c)
print("b != c is ", b != c)
print("b < c is ", b < c)
print("b > c is ", b > c)
print("b <= c is ", b <= c)
print("b >= c is ", b >= c)

# Logical operators
d = 3
e = 7
print("d < 5 and e > 5 is ", d < 5 and e > 5)
print("d < 5 or e > 5 is ", d < 5 or e > 5)
print("not(d < 5 and e > 5) is ", not(d < 5 and e > 5))

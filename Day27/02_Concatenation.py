# Concatenate
l=[10,20,30,40,50,60]
s=["Alice","Bob","Mike",11,22,33]
r=l+s
print(r)
print()

l=[10,20,30,40,50,60]
s=["Alice","Bob","Mike",11,22,33]
p=[22.47,44.5,True,False]
r=l+s+p
print(r)
print()

# Repetition of lists
l=[10,20,30,40,50]
r=l*5
print(r)
print()

# Membership in lists(in,not in)
l=[10,20,30,40,50]
print(20 in l)
print(33 not in l)
print(35 in l)
print(30 not in l)

# program to check whether the student is pass or fail by user input

# if

marks = int(input("Enter your marks \n"))

if marks<=33:
    print("fail \n")

if marks>33:
    print("Pass \n")

# if else
marks = int(input("Enter your marks \n"))
if marks<=33:
    print("Fail \n")

else:
    print("Pass \n")


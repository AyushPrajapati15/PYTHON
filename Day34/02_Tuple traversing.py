# Traversing operation on tuple
# Traverse using for loop
t=(10,20,30,40,50)
for i in t:
    print(i)
print()

t=(10,20,30,40,50)
for i in t:
    print(t)
print()

# Traverse using while loop
t=(10,20,30,40,50)
i=0
while i<len(t):
    print(t[i])
    i=i+1
print()

